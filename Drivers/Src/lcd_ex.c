		lcd_write_reg(0x0036,0x0008);
		lcd_write_reg(0x0037,0x0406); 
		lcd_write_reg(0x0038,0x0304);//0200
		lcd_write_reg(0x0039,0x0007); 
		lcd_write_reg(0x003C,0x0602);// 0504
		lcd_write_reg(0x003D,0x0008); 
		//ram
